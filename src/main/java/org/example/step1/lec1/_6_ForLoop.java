package org.example.step1.lec1;

public class _6_ForLoop {
    public static void main(String[] args) {
        //for loop is a control flow statement that allows a block of code to be executed repeatedly. It is commonly used when the number of iterations is known beforehand.
        //for (initialization; termination; increment) { }
        for (int i = 1; i <= 5; i++) {
            System.out.println("Iteration: " + i);
        }
    }
}
